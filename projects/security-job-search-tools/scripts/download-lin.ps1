        # Get the page
        $response = Invoke-WebRequest -Uri $url -UseBasicParsing -TimeoutSec 30
        $html = $response.Content
        
        # Extract image src
        if ($html -match '<img[^>]*src=["'']([^"'']+)["'']') {
            $imgSrc = $matches[1]
            
            # Make it absolute URL
            if ($imgSrc -notlike 'http*') {
                if ($imgSrc -like '/*') {
                    $imgUrl = "https://www.t-x-m.com$imgSrc"
                } else {
                    $imgUrl = "https://www.t-x-m.com/$imgSrc"
                }
            } else {
                $imgUrl = $imgSrc
            }
            
            Write-Host "  Image URL: $imgUrl"
            
            # Download the image
            Invoke-WebRequest -Uri $imgUrl -OutFile $outputFile -UseBasicParsing -TimeoutSec 30
            
            if (Test-Path $outputFile) {
                $fileInfo = Get-Item $outputFile
                Write-Host "  Success: $([math]::Round($fileInfo.Length/1KB, 2)) KB"
